function syncChrome() {
  const data = content[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = data.dir;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = data[key] ?? node.textContent;
  });
  langToggle.textContent = data.langLabel;
}

function syncHeaderState() {
  const hasHero = Boolean(app.querySelector(".hero"));
  const shouldBlend = hasHero && window.scrollY < 56;
  document.body.classList.toggle("header-blended", shouldBlend);
  if (siteHeader) {
    siteHeader.classList.toggle("is-detached", !shouldBlend);
  }
  if (topbar) {
    topbar.classList.toggle("is-detached", !shouldBlend);
  }
}

